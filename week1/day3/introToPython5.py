firstresponse = int(input("what is your number?"))

secondresponse = int(input("what is your second number?"))

thirdresponse = input("what is your operation?")

if thirdresponse == "+":
    print(firstresponse + secondresponse)
elif thirdresponse == "-":
    print(firstresponse - secondresponse)
elif thirdresponse == "*":
    print(firstresponse * secondresponse)
elif thirdresponse == "/":
    print(firstresponse / secondresponse)
